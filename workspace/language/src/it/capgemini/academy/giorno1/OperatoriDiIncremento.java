package it.capgemini.academy.giorno1;

public class OperatoriDiIncremento {
	public static void main(String[] args) {
		int x = 2;
		int y = 11;
		System.out.println(y%x); 
		// operatore modulo. restituisce il resto della divisione euclidea
		
		x++;
		//incrementa x di 1, equivale a x=x+1
		x--;
		x += 10;
		// equivale a x = x + 10;
		// allo stesso modo per le altre operazioni
		
		
	}

	
	
}
